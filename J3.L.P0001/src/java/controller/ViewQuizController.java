    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        HttpSession session = request.getSession();
        
        //get account saved from session to get user id
        AccountEntity account = (AccountEntity) session.getAttribute("account");
        int accountId = account.getId();
        
        //get all list question of user
        Vector<QuestionEntity> listQuestion = new QuestionDAO().getListQuesion(accountId);
        
        //push data to request atrribute and display them
        request.setAttribute("listQues", listQuestion);
        request.setAttribute("numbersQuestion", listQuestion.size());
        
        request.getRequestDispatcher("managerQuiz.jsp").forward(request, response);
    }

